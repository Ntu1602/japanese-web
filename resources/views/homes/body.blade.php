                <h2>3.&nbsp;Kanji cho người mới bắt đầu</h2>
                <p>Các bạn mới bắt đầu làm quen với bảng chữ cái Kanji nhưng chưa biết phương pháp học&nbsp; dễ hiểu
                    cũng như nhớ lâu. Có rất nhiều cách học kajii cho người mới bắt đầu như học Kanji&nbsp; qua các bộ,
                    các chữ tượng hình. Để học Kanji tốt bạn tham khảo 6 bước học Kanji cho người mới bắt đầu để tìm ra
                    được cách học hiệu quả cho mình nhé.</p>
                    <br>
                <h3>3.1. Cách bước học Kanji</h3>
                <p>Bước 1 : In danh sách bạn kanji cần học ra.</p>
                <p>Bước 2 : Bạn nên đọc âm Hán việt và nhìn chữ kanji (không cần thuộc) Mỗi ngày đọc 5 lần cũng được
                    (mỗi ngày 1 trang) và tăng dần.</p>
                <p>Bước 3 :&nbsp; Cứ cầm danh sách mà đọc (đọc như đọc kinh vậy, không cần thuộc nhưng đọc hoài ắc nó tự
                    thuộc, theo kiểu mưa dầm thấm đất).</p>
                <p>Bước 4 : Bạn đọc hoài sẽ phát hiện ra những chữ Hán việt giống nhau hoặc là gần âm với nhau sẽ có chữ
                    hoặc bộ giống nhau nhớ là không cần học viết.</p>
                <p>Bước 5 : Tự nghiên cứu ra quy luật chuyển từ Hán việt sang Hán Nhật (âm On) Có thể Theo quy tắc bên
                    dưới</p>
                <p>Bước 6 : Học âm Kun như vậy bạn nhìn vào hán việt đã chuyển sang được rồi cách học này khoảng 3 tháng
                    bạn thuộc khoảng 2000 từ và tự chuyển thành thạo nhé.</p>
                    <br>
                <h3>3.2.&nbsp;Làm quen với 10 chữ đầu tiên</h3>
                <p>Dưới đây bạn sẽ được làm quen với 10 chữ kanji khi bắt đầu học tiếng Nhật. Bạn hãy áp dụng các học
                    trên trong 2 ngày đầu với 10 từ này xem hiệu quả như thế nào nhé.</p>
                    <br>
                <p>1, 日 nhật = mặt trời: Vẽ hình mặt trời</p>
                <p>2, 山 sơn = núi&nbsp; : vẽ hình 3 ngọn núi</p>
                <p>3, 木 mộc = cây : vẽ hình cái cây</p>
                <p>4, 川 xuyên = hình con sông</p>
                <p>5, 行 hành, hàng = chữ vẽ hình đường đi</p>
                <p>6, 雨 vũ = mưa: vẽ hình trời mưa</p>
                <p>7, 中 trung = giữa: Chặt đôi ở giữa</p>
                <p>8, 長 trường = dài: Tượng hình tóc dài của người già</p>
                <p>9, 高 cao = hình ảnh lầu cao</p>
                <p>10, 東 đông = mặt trời (日) ló sau cây (木)</p>
                <br>
                <p>Muốn học tốt Kanji thì trong tư tưởng không được vội bạn phải có thời gian để hiểu và tìm ra được
                    cách học phù hợp với bản thân mình nữa. Trên đây là&nbsp;6 bước học Kanji cho người mới bắt đầu mình
                    chia sẽ để bạn tham khảo thêm. Chúc các bạn tìm được cách học kanji tiếng Nhật hiệu quả nhất.</p>
