                <p>4, メリークリスマス&nbsp; (Merii Kurisumasu) : &nbsp;Giáng sinh vui vẻ</p>
                <p>5, おめでとう (Omedetou)&nbsp; : &nbsp;Xin chúc mừng</p>
                <br>
                <h3>2.8. Dùng trong trường hợp cần trợ giúp</h3>
                <p>1, 迷ってしまいました (Mayotte shimai mashita) &nbsp;:&nbsp; Tôi bị lạc mất rồi</p>
                <p>2, お手伝いしましょうか (Otetsudai shimashouka) &nbsp;: &nbsp;Tôi có thể giúp gì cho bạn</p>
                <p>3, 手伝ってくれますか (Tetsudatte kuremasuka) : &nbsp;Bạn có thể giúp tôi không</p>
                <p>4, (トイレ/薬局) はどこですか(Toire/yakkyoku) wa doko desuka) : &nbsp;Nhà tắm công cộng/ hiệu thuốc ở đâu vậy
                </p>
                <p>5, まっすぐ行ってください。そして、 左／右にまがってください&nbsp; (Massugu itte kudasai. Soshite, hidari / migi ni magatte
                    kudasai) : Đi thẳng! Sau đó rẽ trái/phải!</p>
                    <br>
                <h3>2.9. Dùng để giải quyết các vấn đề</h3>
                <p>1, すみません (Sumimasen) : &nbsp;Xin lỗi (khi không nghe rõ cái gì)</p>
                <p>2, ごめんなさい &nbsp;(Gomenasai) : &nbsp;Xin lỗi (khi phạm lỗi)</p>
                <p>3, 大丈夫です (Daijyoubu desu)&nbsp; : &nbsp;Không vấn đề gì</p>
                <p>4, もういちど言ってくれますか (Mouichido itte kuremasuka) : &nbsp;Bạn có thể nói lại lần nữa không</p>
                <p>5, ゆっくりしゃべってくれますか (Yukkuri shabette kuremasuka)&nbsp; : &nbsp;Bạn có thể nói chậm lại không</p>
